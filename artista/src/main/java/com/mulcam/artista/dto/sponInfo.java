package com.mulcam.artista.dto;

public class sponInfo {
	String id;
	int fundingNo;
	String projTitle;
	int sum;
	String sponDate;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getFundingNo() {
		return fundingNo;
	}
	public void setFundingNo(int fundingNo) {
		this.fundingNo = fundingNo;
	}
	public String getProjTitle() {
		return projTitle;
	}
	public void setProjTitle(String projTitle) {
		this.projTitle = projTitle;
	}
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}
	public String getSponDate() {
		return sponDate;
	}
	public void setSponDate(String sponDate) {
		this.sponDate = sponDate;
	}
	
	
}
